    public  void tampiltabel(){
    DAOPasien daoPasien = new DAOPasien();
    while(modelpasien.getRowCount()> 0){
    modelpasien.removeRow(0);
    }
    List<Pasien> data = daoPasien.getData();
    Iterator it = data.iterator();
   Pasien pasien = null;
        while (it.hasNext()) {
           pasien = (Pasien) it.next();
          modelpasien.addRow(new Object[]{pasien.getKdPasien(),pasien.getNmPasien(),pasien.getJeniskelamin(),pasien.getUp(),pasien.getAlamat(),pasien.getTlp()});   
        }
    
 }

