    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
      DAOPasien daoPasien = new DAOPasien();
       Pasien p = new Pasien();
      try{
  p.setKdPasien(txtkdpasien.getText());
  p.setNmPasien(txtnmpasien.getText());
    String jekel="";
    if (RBl.isSelected()){
     jekel = "Laki-Laki";
     }
    else{
     jekel = "Perempuan";
         }
   p.setJeniskelamin(jekel);
   p.setUp(txtumr.getText());
   p.setAlamat(txtalt.getText());
   p.setTlp(txttlp.getText());
    
    daoPasien.simpanPasien(p);
    JOptionPane.showMessageDialog (null, "Berhasil Menyimpan", "Title", JOptionPane.INFORMATION_MESSAGE);
bersih();
      } 
      catch (Exception E){
       JOptionPane.showMessageDialog (null, "Gagal Menyimpan", "Title", JOptionPane.INFORMATION_MESSAGE);
      }
      tampiltabel();
    }//GEN-LAST:event_btnsimpanActionPerformed

