    private void txtkdpasienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtkdpasienActionPerformed
   DAOPasien daoPasien = new DAOPasien();
String a = txtkdpasien.getText();
try{
  daoPasien.cariPasienByKd(a);
  Pasien pasien = daoPasien.cariPasienByKd(a);
  txtnmpasien.setText(pasien.getNmPasien());
  String jk = pasien.getJeniskelamin();
  if(jk.equals("Laki-Laki")){
      RBl.setSelected(true);
  }
  else{
  RBP.setSelected(true);
   }
  txtumr.setText(pasien.getUp());
  txtalt.setText(pasien.getAlamat());
  txttlp.setText(pasien.getTlp());
}catch(Exception e){
JOptionPane.showMessageDialog (null, "Kode Dokter Tersebut Tidak Ditemukan", "Title", JOptionPane.INFORMATION_MESSAGE);
         bersih();
}
    }//GEN-LAST:event_txtkdpasienActionPerformed

