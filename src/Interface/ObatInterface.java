      public  void tampiltabel(){
   DAOObat daoObat = new DAOObat();
    while(modelobat.getRowCount()> 0){
    modelobat.removeRow(0);
    }
    List<Obat> data = daoObat.getData();
    Iterator it = data.iterator();
    Obat obat = null;
        while (it.hasNext()) {
           obat = (Obat) it.next();
          modelobat.addRow(new Object[]{obat.getKdObat(),obat.getNmObat(),obat.getJenisobat(),obat.getTglkadaluarsa(),obat.getHarga()});   
        }
    
 }

