    private void txtkdobatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtkdobatActionPerformed
       DAOObat daoObat = new DAOObat();
String a = txtkdobat.getText();
try{
  daoObat.cariObatByKd(a);
  Obat obat = daoObat.cariObatByKd(a);
  txtnmobat.setText(obat.getNmObat());
  String jo = obat.getJenisobat();
   if(jo.equals("kapsul")){
     cmbjo.setSelectedIndex(1);
     }
     if(jo.equals("tablet")){
     cmbjo.setSelectedIndex(2);
     }
     if(jo.equals("cair")){
     cmbjo.setSelectedIndex(3);
     }
    String dateValue = obat.getTglkadaluarsa();
SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
Date tgl = sdf.parse(dateValue);
tglkadaluarsa.setDate(tgl);
txtharga.setText(obat.getHarga());
}catch (Exception E){}
    }//GEN-LAST:event_txtkdobatActionPerformed

