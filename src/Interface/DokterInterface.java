    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
       DAODokter daoDokter = new DAODokter();
       Dokter dr = new Dokter();
      try{
    dr.setKdDokter(txtkddokter.getText());
    dr.setNmDokter(txtnmdokter.getText());
     String jekel="";
    if (RBl.isSelected()){
     jekel = "Laki-Laki";
     }
    else{
     jekel = "Perempuan";
         }
    dr.setJeniskelamin(jekel);
    dr.setSpecialist(txtspecial.getText());
    dr.setAlamat(txtalt.getText());
    dr.setTlp(txttlp.getText());
    dr.setPassword(txtpass.getText());
    
    daoDokter.simpanDokter(dr);
    JOptionPane.showMessageDialog (null, "Berhasil Di Menyimpan", "Title", JOptionPane.INFORMATION_MESSAGE);
    bersih();
   
      } 
      catch (Exception E){
       JOptionPane.showMessageDialog (null, "Gagal Di Menyimpan", "Title", JOptionPane.INFORMATION_MESSAGE);
       
      }
       tampiltabel();
    }//GEN-LAST:event_btnsimpanActionPerformed

