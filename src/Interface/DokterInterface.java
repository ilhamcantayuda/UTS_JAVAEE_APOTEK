    private void txtkddokterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtkddokterActionPerformed
DAODokter daoDokter = new DAODokter();
String a = txtkddokter.getText();
try{
  daoDokter.cariDokterByKd(a);
  Dokter dokter = daoDokter.cariDokterByKd(a);
  txtnmdokter.setText(dokter.getNmDokter());
  String jk = dokter.getJeniskelamin();
  if(jk.equals("Laki-Laki")){
      RBl.setSelected(true);
  }
  else{
  RBP.setSelected(true);
   }
  txtspecial.setText(dokter.getSpecialist());
  txtalt.setText(dokter.getAlamat());
  txttlp.setText(dokter.getTlp());
  txtpass.setText(dokter.getPassword());
}
catch (Exception e){
         JOptionPane.showMessageDialog (null, "Kode Dokter Tersebut Tidak Ditemukan", "Title", JOptionPane.INFORMATION_MESSAGE);
         bersih();
     }
      
    }//GEN-LAST:event_txtkddokterActionPerformed

